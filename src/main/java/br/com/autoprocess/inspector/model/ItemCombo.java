package br.com.autoprocess.inspector.model;

public class ItemCombo {

	private String valor;

	public ItemCombo(String valor) {
		super();
		this.valor = valor;
	}

	public String getValor() {
		return valor;
	}

	public void setValor(String valor) {
		this.valor = valor;
	}

	public String toString() {
		return valor;
	}
	
}
